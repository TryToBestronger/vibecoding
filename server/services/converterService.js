function toSimplifiedChinese(text) {
  if (!text || typeof text !== 'string') {
    return text;
  }
  
  let result = text;
  for (const [traditional, simplified] of Object.entries(traditionalToSimplified)) {
    result = result.replace(new RegExp(traditional, 'g'), simplified);
  }
  return result;
}

function convertHotspotToSimplified(hotspot) {
  return {
    ...hotspot,
    title: toSimplifiedChinese(hotspot.title),
    content: hotspot.content ? toSimplifiedChinese(hotspot.content) : hotspot.content
  };
}

